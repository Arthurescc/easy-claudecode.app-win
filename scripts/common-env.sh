#!/bin/zsh
set -euo pipefail

_common_env_self="${(%):-%x}"
SCRIPT_DIR="$(cd "$(dirname "$_common_env_self")" && pwd)"
REPO_ROOT="${EASY_CLAUDECODE_ROOT:-$(cd "$SCRIPT_DIR/.." && pwd)}"
EASY_CLAUDECODE_HOME="${EASY_CLAUDECODE_HOME:-$HOME/.easy-claudecode}"
LOCAL_ENV_FILE="${EASY_CLAUDECODE_ENV_FILE:-$REPO_ROOT/.env}"

if [[ -f "$LOCAL_ENV_FILE" ]]; then
  set -a
  source "$LOCAL_ENV_FILE"
  set +a
fi

export EASY_CLAUDECODE_ROOT="$REPO_ROOT"
export EASY_CLAUDECODE_HOME="${EASY_CLAUDECODE_HOME:-$HOME/.easy-claudecode}"

export CLAUDE_CONSOLE_SOURCE_ROOT="${CLAUDE_CONSOLE_SOURCE_ROOT:-$REPO_ROOT}"
export CLAUDE_CONSOLE_FRONTEND_ROOT="${CLAUDE_CONSOLE_FRONTEND_ROOT:-$REPO_ROOT/apps/web}"
export CLAUDE_CONSOLE_RUNTIME_ROOT="${CLAUDE_CONSOLE_RUNTIME_ROOT:-$EASY_CLAUDECODE_HOME/runtime/claude-console}"
export CLAUDE_CONSOLE_UPLOAD_ROOT="${CLAUDE_CONSOLE_UPLOAD_ROOT:-$EASY_CLAUDECODE_HOME/uploads}"
export CLAUDE_CHAT_META_FILE="${CLAUDE_CHAT_META_FILE:-$EASY_CLAUDECODE_HOME/claude-chat-meta.json}"

export CLAUDE_ROUTER_SOURCE_DIR="${CLAUDE_ROUTER_SOURCE_DIR:-$REPO_ROOT/config/router}"
export CLAUDE_ROUTER_RUNTIME_DIR="${CLAUDE_ROUTER_RUNTIME_DIR:-$EASY_CLAUDECODE_HOME/router}"
export CLAUDE_ROUTER_CONFIG_FILE="${CLAUDE_ROUTER_CONFIG_FILE:-$CLAUDE_ROUTER_RUNTIME_DIR/config.json}"
export CLAUDE_ROUTER_CUSTOM_FILE="${CLAUDE_ROUTER_CUSTOM_FILE:-$CLAUDE_ROUTER_RUNTIME_DIR/custom-router.js}"

export CLAUDE_CONSOLE_LOG_ROOT="${CLAUDE_CONSOLE_LOG_ROOT:-$EASY_CLAUDECODE_HOME/logs}"
export CLAUDE_CONSOLE_HOST="${CLAUDE_CONSOLE_HOST:-127.0.0.1}"
export CLAUDE_CONSOLE_PORT="${CLAUDE_CONSOLE_PORT:-18882}"
export CLAUDE_ROUTER_HEALTH_URL="${CLAUDE_ROUTER_HEALTH_URL:-http://127.0.0.1:3456/health}"
export CLAUDE_PROXY_HEALTH_URL="${CLAUDE_PROXY_HEALTH_URL:-http://127.0.0.1:3460/health}"

export CLAUDE_WEB_PERMISSION_MODE="${CLAUDE_WEB_PERMISSION_MODE:-bypassPermissions}"
export CLAUDE_CODE_EXPERIMENTAL_AGENT_TEAMS="${CLAUDE_CODE_EXPERIMENTAL_AGENT_TEAMS:-1}"
export CLAUDE_WORKSPACE_ROOT="${CLAUDE_WORKSPACE_ROOT:-$REPO_ROOT}"
export CLAUDE_WRAPPER_PATH="${CLAUDE_WRAPPER_PATH:-$REPO_ROOT/scripts/claude-local-router.sh}"
export CLAUDE_REAL_BIN="${CLAUDE_REAL_BIN:-$(command -v claude || true)}"
export CLAUDE_TMUX_BIN="${CLAUDE_TMUX_BIN:-$(command -v tmux || echo "$HOME/.local/bin/tmux")}"

if [[ -z "${CLAUDE_EXTRA_ALLOWED_DIRS:-}" ]]; then
  export CLAUDE_EXTRA_ALLOWED_DIRS="$REPO_ROOT,$HOME/Desktop"
fi

mkdir -p "$EASY_CLAUDECODE_HOME" "$CLAUDE_CONSOLE_LOG_ROOT" "$CLAUDE_CONSOLE_UPLOAD_ROOT" "$CLAUDE_ROUTER_RUNTIME_DIR"

unset HTTP_PROXY HTTPS_PROXY ALL_PROXY http_proxy https_proxy all_proxy
export NO_PROXY="127.0.0.1,localhost"
export no_proxy="127.0.0.1,localhost"
export PATH="$HOME/.local/node-current/bin:$HOME/.local/bin:$PATH"

